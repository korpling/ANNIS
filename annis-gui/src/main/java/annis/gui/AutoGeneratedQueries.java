package annis.gui;

import annis.examplequeries.ExampleQuery;
import annis.gui.controlpanel.ControlPanel;
import annis.gui.controlpanel.CorpusListPanel;
import annis.gui.controlpanel.QueryPanel;
import annis.libgui.Helper;
import com.sun.jersey.api.client.GenericType;
import com.sun.jersey.api.client.WebResource;
import com.vaadin.data.Property;
import com.vaadin.event.ItemClickEvent;
import com.vaadin.sass.internal.util.StringUtil;
import com.vaadin.ui.Notification;
import com.vaadin.ui.Table;
import java.util.List;
import org.apache.commons.lang3.StringUtils;
import org.slf4j.Logger;
import org.slf4j.LoggerFactory;

/**
 * Wraps the auto generated queries.
 *
 * @author Benjamin Wei√üenfels <b.pixeldrama@gmail.com>
 */
public class AutoGeneratedQueries extends Table
{

  // first column String
  private final String EXAMPLE_QUERY = "example query";

  //second column String
  private final String DESCRIPTION = "description";

  //main ui window
  private final SearchUI ui;

  // holds the current examples
  private List<ExampleQuery> examples;

  // holds the name of the selected corpora
  private String[] selectedCorpora;

  // gets the
  private final static Logger log = LoggerFactory.
    getLogger(AutoGeneratedQueries.class);

  public AutoGeneratedQueries(String caption, SearchUI ui)
  {
    super(caption);
    this.ui = ui;
  }

  /**
   * Sets some layout properties.
   */
  private void setUpTable()
  {

    // expand the table
    setSizeFull();

    // Allow selecting items from the table.
    setSelectable(true);

    // Send changes in selection immediately to server.
    setImmediate(true);



    // set clickhandler for execute example query
    addListener(new ExampleQueryExecuter());
  }

  @Override
  public void attach()
  {
    super.attach();

    setUpTable();

    loadExamplesFromRemote();

    addItems();
  }

  private void addItems()
  {
    super.attach();
    addContainerProperty(EXAMPLE_QUERY, String.class,
      "tok=\"in\" & tok=\"Zossen\" & #1 .1 #2");
    addContainerProperty(DESCRIPTION, String.class,
      "searches for token in followed by Zossen in <a href=\"./\">pccv2</a>");

    if (examples != null)
    {
      for (int i = 0; i < examples.size(); i++)
      {
        ExampleQuery example = examples.get(i);
        addItem(new Object[]
        {
          example.getExampleQuery(), example.getDescription()
        }, new Integer(i));
      }
    }
  }

  /**
   * Catches click events on the example query column.
   *
   * TODO Should linked the corpus browser for a speficic corpus, which is
   * mentioned in the description
   *
   */
  private class ExampleQueryExecuter implements ItemClickEvent.ItemClickListener
  {

    @Override
    public void itemClick(ItemClickEvent event)
    {
      if (event.getButton() == ItemClickEvent.BUTTON_LEFT)
      {
        String column = (String) event.getPropertyId();
        ControlPanel controlPanel = ui.getControlPanel();
        QueryPanel queryPanel;

        if (controlPanel == null)
        {
          log.error("controlPanel is not initialized");
          return;
        }

        queryPanel = controlPanel.getQueryPanel();
        if (queryPanel == null)
        {
          log.error("queryPanel is not initialized");
          return;
        }

        if (EXAMPLE_QUERY.equals(column))
        {
          Property query = event.getItem().getItemProperty(column);
          queryPanel.setQuery(query.toString());
        }
      }
    }
  }

  /**
   * Loads all available example queries.
   */
  private void loadExamplesFromRemote()
  {
    loadExamplesFromRemote(null);
  }

  /**
   * Loads the available example queries for a specific corpus.
   *
   * @param corpusName
   */
  private void loadExamplesFromRemote(String[] corpusNames)
  {
    WebResource annisWebResource = Helper.getAnnisWebResource();
    try
    {
      if (corpusNames == null)
      {
        examples = annisWebResource.path("query").path("corpora").path(
          "example-queries").get(new GenericType<List<ExampleQuery>>()
        {
        });
      }
      else
      {
        examples = annisWebResource.path("query").path("corpora").path(
          "example-queries").queryParam("corpora",
          StringUtils.join(corpusNames, ",")).get(
          new GenericType<List<ExampleQuery>>()
        {
        });
      }
    }
    catch (Exception ex)
    {
      Notification.show("could not load example quries", ex.getMessage(),
        Notification.Type.ERROR_MESSAGE);
    }

  }

  /**
   * Reads the corpora from the {@link CorpusListPanel}.
   */
  public void addCorpora(List<ExampleQuery> examples)
  {
    this.examples = examples;
  }

  /**
   * Sets the selected corpora.
   *
   * @param selectedCorpus if differs from {@link #selectedCorpus} this causes a
   * load/reload of all example queries for the selected corpus. If it is null,
   * all available example queries are fetched.
   */
  public void setSelectedCorpus(String[] selectedCorpora)
  {
    if (this.selectedCorpora == null)
    {
      this.selectedCorpora = selectedCorpora;
      loadExamplesFromRemote(selectedCorpora);
    }
  }
}
